package Basic_Cheeze;

public class IncreDecre {
    static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        x += 6; //similar to x = x + 6
        System.out.println(x);

        x *= 4; //similar to x = x * 6
        System.out.println(x);

        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);


    }
}
